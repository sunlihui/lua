-- 数据类型
-- 6种数据类型
--
-- nil    表示不存在的空对象或者无效值，类似python的None
-- boolean    布尔类型，取值为true或者false
-- number    数字类型，不区分整数和浮点数
-- string    字符串类型
-- function    函数类型
-- table    表类型，非常灵活的数据结构
--
print(type(nil))
print(type(true))
print(type(123))
print(type(1.23))
print(type('kevin'))
print(type(print))
print(type(table))
